local activePurchases = {}

Citizen.CreateThread(function()
    while not exports['vrp-config']:IsConfigReady() do
        Wait(100)
    end
    local areRegistersPublic = exports['vrp-config']:GetMiscConfig("business.registers.public")
    for id,register in ipairs(activeRegisters) do
        if register.public and not areRegistersPublic then goto continue end
        if register.whitelistedOnly and exports['vrp-config']:GetServerType() ~= "whitelisted" then goto continue end

        local ptId = "vrp-business:register_" .. id
        exports["vrp-polytarget"]:AddBoxZone(ptId, register.polytarget[1], register.polytarget[2], register.polytarget[3], register.polytarget[4])
        exports['vrp-interact']:AddPeekEntryByPolyTarget(ptId, {{
            event = "vrp-business:registerPurchasePrompt",
            id = ptId .. "_purchase",
            icon = "cash-register",
            label = "make payment",
            parameters = {registerId = id, business = register.business}
        }}, { distance = { radius = 2.0 } })

        exports['vrp-interact']:AddPeekEntryByPolyTarget(ptId, {{
            event = "vrp-business:registerChargePrompt",
            id = ptId .. "_charge",
            icon = "credit-card",
            label = "Charge Customer",
            parameters = {registerId = id, business = register.business}
        }}, { distance = { radius = 2.0 }, isEnabled = function(pEntity, pContext) return IsEmployedAt(register.business) end})
        ::continue::
    end
end)

AddEventHandler('vrp-business:registerPurchasePrompt', function(pParameters, pEntity, pContext)
    local activeRegisterId = pParameters.registerId
    local activeRegister = activePurchases[activeRegisterId]
    if not activeRegister or activeRegister == nil then
        TriggerEvent("DoLongHudText", "No purchase active.")
        return
    end
    local priceWithTax = RPC.execute("PriceWithTaxString", activeRegister.cost, "Goods")
    local acceptContext = {{
        title = "Accept Purchase",
        description = "$" .. priceWithTax.text .. " | " .. activeRegister.comment,
        action = "vrp-business:finishPurchasePrompt",
        key = {cost = activeRegister.cost, comment = activeRegister.comment, registerId = pParameters.registerId, business = pParameters.business, charger = activeRegister.charger},
        disabled = false
    }}
    exports['vrp-ui']:showContextMenu(acceptContext)
end)

AddEventHandler('vrp-business:registerChargePrompt', function(pParameters, pEntity, pContext)
    exports['vrp-ui']:openApplication('textbox', {
        callbackUrl = 'vrp-ui:business:charge',
        key = {registerId = pParameters.registerId, business = pParameters.business},
        items = {
          {
            icon = "dollar-sign",
            label = "Cost",
            name = "cost",
          },
          {
            icon = "pencil-alt",
            label = "Comment",
            name = "comment",
          },
        },
        show = true,
    })
end)

--Add to purchases at registerId pos
RegisterNetEvent('vrp-business:activePurchase')
AddEventHandler("vrp-business:activePurchase", function(data)
    activePurchases[data.registerId] = data
end)

--Remove at registerId pos
RegisterNetEvent('vrp-business:closePurchase')
AddEventHandler("vrp-business:closePurchase", function(data)
    activePurchases[data.registerId] = nil
end)

RegisterUICallback('vrp-business:finishPurchasePrompt', function (data, cb)
    cb({ data = {}, meta = { ok = true, message = '' } })
    local success = RPC.execute("vrp-business:completePurchase", data.key)
    if not success then
        TriggerEvent("DoLongHudText", "The purchase could not be completed.")
    end
end)

RegisterUICallback("vrp-ui:business:charge", function(data, cb)
    cb({ data = {}, meta = { ok = true, message = '' } })
    exports['vrp-ui']:closeApplication('textbox')
    local cost = tonumber(data.values.cost)
    local comment = data.values.comment
    --check if cost is actually a number
    if cost == nil or not cost then return end
    if comment == nil then comment = "" end

    if cost < 1 then cost = 1 end --Minimum $1

    --Send event to everyone indicating a purchase is ready at specified register
    RPC.execute("vrp-business:startPurchase", {cost = cost, comment = comment, registerId = data.key.registerId, business = data.key.business})
end)
