local raket = {}
function check()
    for k,v in pairs(MODELS_OBJECT.UNSORTED_THREE) do
        local found = false
        local name = v
        for i,o in pairs(compare) do
            if name == o then
                found = true
            end
        end

        if not found then
            raket[name] = true
        end
    end

    TriggerServerEvent("SaveRemovedObjects",raket)
end

PLACED_LIST = {}
HASH_MODELS = {}
DISPLAY_NAMES = {}
local failed = 0
local added = 1

local alreadyBuilt = false
function readyModels(force)
    if alreadyBuilt and not force then return end
    alreadyBuilt = true

    if force then
        PLACED_LIST = {}
        HASH_MODELS = {}
        failed = 0
        added = 1
    end

    CURRENT_REMOVED = {}

    for u, j in pairs(MODELS_OBJECT) do
        local addedThisLoop = 1
        for k,v in pairs(j) do
            local model = nil --GetHashKey(v)
            local displayName = nil
            local modelUnhashed = nil

            if type(v) == 'table' then
                model = GetHashKey(v[1])
                modelUnhashed = v[1]
                displayName = v[2]
                v = model
            else
                modelUnhashed = v
                model = GetHashKey(v)
            end

            if displayName ~= nil then
                DISPLAY_NAMES[modelUnhashed] = displayName
            end
            
            if IsModelValid(model) then
                if PLACED_LIST[model] then
                    --print('Overlap: ' .. model .. ' | ' .. v)
                    failed = failed + 1
                    CURRENT_REMOVED[modelUnhashed] = true
                else
                    if HASH_MODELS[added] == nil then
                        HASH_MODELS[added] = {
                            u,
                            {[addedThisLoop] = {model, modelUnhashed, displayName}} 
                        } 
                    else
                        HASH_MODELS[added][2][addedThisLoop] = {model, modelUnhashed, displayName}
                        
                    end
                    addedThisLoop = addedThisLoop + 1
                    PLACED_LIST[model] = modelUnhashed
                end
                
            else
                CURRENT_REMOVED[modelUnhashed] = true
                failed = failed + 1
            end
        end
        if addedThisLoop ~= 1 then
            added = added + 1
        end
    end
    buildActionObjects(CURRENT_FLAGS)
    print("failed models "..failed)
    failed = 0

end


function buildActionObjects(flags)
    for u, j in pairs(actionObjects) do
        local addedThisLoop = 1
        for k,v in pairs(j) do
            local model = GetHashKey(k)
            if IsModelValid(model) then
                local foundFlag = false
                for r,t in pairs(flags) do
                    for e,q in pairs(v) do
                        if r == e and t then foundFlag = true end
                    end
                end
 
                if foundFlag then
                    if PLACED_LIST[model] then
                        --print('Overlap Action: ' .. model .. ' | ' .. v)
                        failed = failed + 1
                        CURRENT_REMOVED[v] = true
                    else
                        if HASH_MODELS[added] == nil then 
                            HASH_MODELS[added] = {
                                u,
                                {[addedThisLoop] = {model,k}} 
                            } 
                        else
                            HASH_MODELS[added][2][addedThisLoop] = {model,k}
                            
                        end
                        addedThisLoop = addedThisLoop + 1
                        PLACED_LIST[model] = k
                    end
                end

            else
                CURRENT_REMOVED[v] = true
                failed = failed + 1
            end
        end
        if addedThisLoop ~= 1 then
            added = added + 1
        end
    end
end

function clearActionObjects()

    for k,v in pairs(HASH_MODELS) do
        if actionObjects[v[1]] then
            for u,i in pairs(v[2]) do
                PLACED_LIST[i[1]] = nil
            end

            HASH_MODELS[k] = nil
        end
        added = added - 1
    end

