local ActivePlayers = {}

function DoesSourceExist(pPlayerId)
    return ActivePlayers[pPlayerId]
end

RegisterServerEvent("vrp-base:spawnInitialized")
AddEventHandler("vrp-base:spawnInitialized",function()
    local src = source
    ActivePlayers[src] = true
end)

AddEventHandler("playerDropped", function()
    local src = source
    ActivePlayers[src] = nil
end)

